package Poczatek;

public class dzielenieBezReszty {
    public static void main(String[] args) {
        int liczba1 = 37;
        int liczba2 = 11;
        System.out.println(liczba1/liczba2);

        float liczba1a = 2.33f;
        float liczba1b = 5.77f;
        System.out.println("Suma " + (liczba1a + liczba1b));
        System.out.println("Różnica " + (liczba1a - liczba1b));
        System.out.println("Iloczyn " + (liczba1a * liczba1b));
        System.out.println("Iloraz " + (liczba1a / liczba1b));

    }
}
